def redact_tokens(value: Any) -> Any:
    if isinstance(value, dict):
        return {
            key: redact_query_string(item) if key == "queryString" and isinstance(item, str)
            else ("<redacted>" if key in TOKEN_KEYS and item else redact_tokens(item))
            for key, item in value.items()
        }
    if isinstance(value, list):
        return [redact_tokens(item) for item in value]
    return value


def redact_query_string(value: str) -> str:
    pairs = []
    for key, item in parse_qsl(value, keep_blank_values=True):
        pairs.append((key, "<redacted>" if key in TOKEN_KEYS and item else item))
    return urlencode(pairs)


def build_bank_witness(request: dict[str, Any], config: FixtureConfig) -> str:
    payload = request.get("payload") or {}
    tx = request.get("tx") or {}
    reserve_input = find_reserve_input(tx, config)
    if request.get("protocol") != "cellshire.bank.reserve-sign":
        raise ValueError("invalid-protocol")
    if request.get("action") != "borrow" and payload.get("action") != "borrow":
        raise ValueError("unsupported-action")
    if not reserve_input:
        raise ValueError("reserve-input-missing")
    if not payload.get("tx_nonce"):
        raise ValueError("missing-tx-nonce")

    body = {
        "protocol": "cellshire.bank.reserve-witness.fixture",
        "version": 1,
        "action": "borrow",
        "tx_nonce": payload["tx_nonce"],
        "offer_id": payload.get("offer_id"),
        "principal": payload.get("principal"),
        "reserve_outpoint": reserve_input["previousOutput"],
        "digest": request_digest(request),
    }
    return "0x" + json.dumps(body, sort_keys=True, separators=(",", ":")).encode("utf-8").hex()


def find_reserve_input(tx: dict[str, Any], config: FixtureConfig) -> dict[str, Any] | None:
    expected = {
        "txHash": config.reserve_tx_hash.lower(),
        "index": config.reserve_index,
    }
    for item in tx.get("inputs") or []:
        prev = item.get("previousOutput") or {}
        if str(prev.get("txHash", "")).lower() == expected["txHash"] and int(prev.get("index", -1)) == expected["index"]:
            return item
    return None


def request_digest(request: dict[str, Any]) -> str:
    encoded = json.dumps(request, sort_keys=True, separators=(",", ":")).encode("utf-8")
    return "0x" + hashlib.sha256(encoded).hexdigest()


def number_or(value: Any, fallback: float) -> float:
    try:
        number = float(value)
    except (TypeError, ValueError):
        return fallback
    return number


def success(body: dict[str, Any], status: int = 200) -> tuple[int, dict[str, Any]]:
    return status, {"ok": True, **body}


def failure(reason: str, status: int = 400) -> tuple[int, dict[str, Any]]:
    return status, {"ok": False, "reason": reason}


def handle_sign(request: dict[str, Any], config: FixtureConfig) -> tuple[int, dict[str, Any]]:
    try:
        return success({
            "protocol": BANK_RESERVE_SIGNER_RESPONSE_PROTOCOL,
            "version": 1,
            "bankWitness": build_bank_witness(request, config),
            "reserveCell": reserve_cell(config),
        })
    except ValueError as err:
        return failure(str(err))


def handle_borrow_inputs(request: dict[str, Any], config: FixtureConfig) -> tuple[int, dict[str, Any]]:
    if request.get("protocol") != "cellshire.bank.inputs.select":
        return failure("invalid-protocol")
    if request.get("action") != "borrow":
        return failure("unsupported-action")
    wallet = request.get("walletAccount") or {}
    if not wallet.get("address"):
        return failure("missing-wallet")
    collateral_amount = number_or(request.get("collateralAmount"), DEFAULT_COLLATERAL_AMOUNT_CKB)
    if collateral_amount <= 0:
        return failure("invalid-collateral")
    return success({
        "protocol": BANK_INPUT_PROVIDER_RESPONSE_PROTOCOL,
        "version": 1,
        "borrow": {
            "bankReserveCell": reserve_cell(config),
            "collateralCell": collateral_cell(config, collateral_amount),
        },
    })


def handle_repay_inputs(request: dict[str, Any], config: FixtureConfig) -> tuple[int, dict[str, Any]]:
    if request.get("protocol") != "cellshire.bank.inputs.select":
        return failure("invalid-protocol")
    if request.get("action") != "repay":
        return failure("unsupported-action")
    wallet = request.get("walletAccount") or {}
    loan = request.get("loan") or {}
    if not wallet.get("address"):
        return failure("missing-wallet")
    if not loan.get("id"):
        return failure("missing-loan")
    collateral_amount = number_or(loan.get("collateralAmount"), DEFAULT_COLLATERAL_AMOUNT_CKB)
    return success({
        "protocol": BANK_INPUT_PROVIDER_RESPONSE_PROTOCOL,
        "version": 1,
        "repay": {
            "debtCell": debt_cell(config),
            "lockedCollateralCell": locked_collateral_cell(config, collateral_amount),
        },
    })


class FixtureHandler(BaseHTTPRequestHandler):
    server_version = "CellshireBankReserveFixture/1.0"

    def do_OPTIONS(self) -> None:
        self._send_json(204, {})

    def do_GET(self) -> None:
        parsed = urlparse(self.path)
        if parsed.path == "/health":
            self._send_json(*success({
                "service": "cellshire-bank-reserve-signer-fixture",
            }))
        elif parsed.path == "/reserve-inputs":
            self._send_json(*success({
                "borrow": {
                    "bankReserveCell": reserve_cell(self.server.config),
                },
                "params": reserve_input_params(self.server.config),
            }))
        elif parsed.path == "/smoke-params":
            query = parse_qs(parsed.query)
            game_url = (query.get("game") or [""])[0]
            self._send_json(*success(smoke_config(self.server.config, game_url)))
        else:
            self._send_json(*failure("not-found", 404))

    def do_POST(self) -> None:
        if self.server.config.token:
            expected = f"Bearer {self.server.config.token}"
            if self.headers.get("authorization") != expected:
                self._send_json(*failure("unauthorized", 401))
                return

        parsed = urlparse(self.path)
        if parsed.path not in {"/sign", "/borrow-inputs", "/repay-inputs"}:
            self._send_json(*failure("not-found", 404))
            return

        try:
            length = int(self.headers.get("content-length") or 0)
            body = self.rfile.read(length).decode("utf-8")
            request = json.loads(body or "{}")
        except (UnicodeDecodeError, json.JSONDecodeError):
            self._send_json(*failure("invalid-json"))
            return

        if parsed.path == "/sign":
            self._send_json(*handle_sign(request, self.server.config))
        elif parsed.path == "/borrow-inputs":
            self._send_json(*handle_borrow_inputs(request, self.server.config))
        else:
            self._send_json(*handle_repay_inputs(request, self.server.config))

    def log_message(self, fmt: str, *args: Any) -> None:
        sys.stderr.write("[bank-reserve-fixture] " + fmt % args + "\n")

    def _send_json(self, status: int, body: dict[str, Any]) -> None:
        data = b"" if status == 204 else json.dumps(body, sort_keys=True).encode("utf-8")
        self.send_response(status)
        self.send_header("access-control-allow-origin", "*")
        self.send_header("access-control-allow-methods", "GET, POST, OPTIONS")
        self.send_header("access-control-allow-headers", "authorization, content-type")
        self.send_header("content-type", "application/json")
        self.send_header("content-length", str(len(data)))
        self.end_headers()
        if data:
            self.wfile.write(data)


