def main(argv: list[str] | None = None) -> int:
    args = parse_args(argv or sys.argv[1:])
    if args.self_test:
        return self_test()

    try:
        deployment_values = load_deployment_values(args.deployment_values_json)
        script_params = script_params_from_json(deployment_values, "deployment-values-json") if deployment_values else dict(SMOKE_SCRIPT_PARAMS)
        if args.script_params_json:
            script_params = load_script_params(args.script_params_json)
    except (OSError, ValueError, json.JSONDecodeError) as err:
        print(f"Invalid deployment config: {err}", file=sys.stderr)
        return 2

    input_provider_url = args.bank_input_provider_url or backend_value(deployment_values, "inputProviderUrl")
    borrow_input_provider_url = args.bank_borrow_input_provider_url or backend_value(deployment_values, "borrowInputProviderUrl")
    repay_input_provider_url = args.bank_repay_input_provider_url or backend_value(deployment_values, "repayInputProviderUrl")
    reserve_signer_url = args.bank_reserve_signer_url or backend_value(deployment_values, "reserveSignerUrl")
    token = args.token or backend_value(deployment_values, "token")

    config = FixtureConfig(
        host=args.host,
        port=args.port,
        reserve_tx_hash=args.reserve_tx_hash.lower(),
        reserve_index=args.reserve_index,
        reserve_amount_ckb=args.reserve_amount_ckb,
        collateral_tx_hash=args.collateral_tx_hash.lower(),
        collateral_index=args.collateral_index,
        debt_tx_hash=args.debt_tx_hash.lower(),
        debt_index=args.debt_index,
        locked_collateral_tx_hash=args.locked_collateral_tx_hash.lower(),
        locked_collateral_index=args.locked_collateral_index,
        script_params=script_params,
        input_provider_url=input_provider_url,
        borrow_input_provider_url=borrow_input_provider_url,
        repay_input_provider_url=repay_input_provider_url,
        reserve_signer_url=reserve_signer_url,
        production_smoke=args.production_smoke,
        bank_reserve_cells=deployment_cells(deployment_values, "bankReserveCells", "reserveCells"),
        borrower_collateral_cells=deployment_cells(deployment_values, "borrowerCollateralCells", "collateralCells"),
        require_deployment_values=bool(args.deployment_values_json),
        token=token,
    )
    if config.production_smoke or args.validate_production_smoke or args.write_smoke_report:
        issues = production_smoke_issues(config)
        if issues:
            print("Invalid production smoke config:", ", ".join(issues), file=sys.stderr)
            return 2
    if args.write_smoke_report:
        smoke = smoke_config(replace(config, production_smoke=True))
        try:
            with open(args.write_smoke_report, "w", encoding="utf-8") as handle:
                json.dump(smoke_execution_report(replace(config, production_smoke=True), smoke), handle, indent=2, sort_keys=True)
                handle.write("\n")
        except OSError as err:
            print(f"Unable to write smoke report: {err}", file=sys.stderr)
            return 2
        if not args.validate_production_smoke:
            print(f"Wrote testnet smoke report: {args.write_smoke_report}")
            return 0
    if args.validate_production_smoke:
        print(json.dumps(smoke_config(replace(config, production_smoke=True)), indent=2, sort_keys=True))
        return 0

    server = FixtureServer((config.host, config.port), FixtureHandler, config)
    print(f"Cellshire bank reserve signer fixture listening on http://{config.host}:{config.port}")
    print(f"  reserve inputs: http://{config.host}:{config.port}/reserve-inputs")
    print(f"  borrow inputs:  http://{config.host}:{config.port}/borrow-inputs")
    print(f"  repay inputs:   http://{config.host}:{config.port}/repay-inputs")
    print(f"  smoke params:   http://{config.host}:{config.port}/smoke-params")
    print(f"  signer URL:     http://{config.host}:{config.port}/sign")
    try:
        server.serve_forever()
    except KeyboardInterrupt:
        print("\nStopping bank reserve signer fixture")
    finally:
        server.server_close()
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
