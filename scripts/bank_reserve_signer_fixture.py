def load_json_file(path: str) -> Any:
    with open(path, "r", encoding="utf-8") as handle:
        return json.load(handle)


def load_script_params(path: str) -> dict[str, str]:
    if not path:
        return dict(SMOKE_SCRIPT_PARAMS)
    return script_params_from_json(load_json_file(path), "script-params-json")


def script_params_from_json(loaded: Any, label: str = "script params") -> dict[str, str]:
    source = loaded.get("params") if isinstance(loaded, dict) and isinstance(loaded.get("params"), dict) else loaded
    if isinstance(loaded, dict) and isinstance(loaded.get("scriptParams"), dict):
        source = loaded["scriptParams"]
    if not isinstance(source, dict):
        raise ValueError(f"{label} must be a JSON object")
    params: dict[str, str] = {}
    for key, value in source.items():
        if not str(key).startswith("chainBank"):
            continue
        if value is None:
            continue
        params[str(key)] = str(value)
    return params


def load_deployment_values(path: str) -> dict[str, Any]:
    if not path:
        return {}
    loaded = load_json_file(path)
    if not isinstance(loaded, dict):
        raise ValueError("deployment-values-json must be a JSON object")
    return loaded


def backend_value(values: dict[str, Any], key: str) -> str:
    backend = values.get("backend") if isinstance(values.get("backend"), dict) else {}
    value = backend.get(key) or values.get(key) or ""
    return str(value) if value is not None else ""


def deployment_cells(values: dict[str, Any], key: str, fallback_key: str = "") -> list[dict[str, Any]]:
    funding = values.get("funding") if isinstance(values.get("funding"), dict) else {}
    raw = funding.get(key)
    if raw is None and fallback_key:
        raw = funding.get(fallback_key)
    if raw is None:
        raw = values.get(key)
    if raw is None:
        return []
    if isinstance(raw, dict):
        raw = [raw]
    if not isinstance(raw, list):
        return []
    return [item for item in raw if isinstance(item, dict)]


def production_smoke_issues(config: FixtureConfig) -> list[str]:
    issues: list[str] = []
    if not (config.input_provider_url or config.borrow_input_provider_url):
        issues.append("missing-production-borrow-input-provider-url")
    elif config.input_provider_url and not valid_url(config.input_provider_url):
        issues.append("invalid-production-input-provider-url")
    elif config.borrow_input_provider_url and not valid_url(config.borrow_input_provider_url):
        issues.append("invalid-production-borrow-input-provider-url")
    if not (config.input_provider_url or config.repay_input_provider_url):
        issues.append("missing-production-repay-input-provider-url")
    elif config.repay_input_provider_url and not valid_url(config.repay_input_provider_url):
        issues.append("invalid-production-repay-input-provider-url")
    if not config.reserve_signer_url:
        issues.append("missing-production-reserve-signer-url")
    elif not valid_url(config.reserve_signer_url):
        issues.append("invalid-production-reserve-signer-url")
    for key in REQUIRED_SCRIPT_PARAM_KEYS:
        if not config.script_params.get(key):
            issues.append(f"missing-{key}")
        elif not HASH_RE.match(config.script_params[key]):
            issues.append(f"invalid-{key}")
    placeholder_values = set(SMOKE_SCRIPT_PARAMS.values())
    for key in REQUIRED_SCRIPT_PARAM_KEYS:
        if config.script_params.get(key) in placeholder_values:
            issues.append(f"placeholder-{key}")
    if not any(config.script_params.get(f"{prefix}TxHash") for prefix in SCRIPT_DEP_PREFIXES):
        issues.append("missing-bank-script-cell-dep")
    for prefix in SCRIPT_DEP_PREFIXES:
        tx_hash = config.script_params.get(f"{prefix}TxHash")
        if tx_hash and not HASH_RE.match(tx_hash):
            issues.append(f"invalid-{prefix}TxHash")
        index = config.script_params.get(f"{prefix}Index")
        if index is not None and not non_negative_integer(index):
            issues.append(f"invalid-{prefix}Index")
        dep_type = config.script_params.get(f"{prefix}Type")
        if dep_type is not None and dep_type not in {"code", "depGroup"}:
            issues.append(f"invalid-{prefix}Type")
    if config.require_deployment_values and not config.bank_reserve_cells:
        issues.append("missing-funded-bank-reserve-cell")
    issues.extend(funded_cell_issues(config.bank_reserve_cells, "bank-reserve-cell"))
    issues.extend(funded_cell_issues(config.borrower_collateral_cells, "borrower-collateral-cell"))
    return issues


def funded_cell_issues(cells: list[dict[str, Any]], label: str) -> list[str]:
    issues: list[str] = []
    for index, cell in enumerate(cells):
        prefix = f"{label}-{index}"
        out_point = cell.get("outPoint") if isinstance(cell.get("outPoint"), dict) else cell
        tx_hash = out_point.get("txHash") or out_point.get("tx_hash")
        if not tx_hash:
            issues.append(f"missing-{prefix}-tx-hash")
        elif not HASH_RE.match(str(tx_hash)):
            issues.append(f"invalid-{prefix}-tx-hash")
        raw_index = out_point.get("index")
        if raw_index is None:
            issues.append(f"missing-{prefix}-index")
        elif not non_negative_integer(str(raw_index)):
            issues.append(f"invalid-{prefix}-index")
        amount = cell.get("amount") if cell.get("amount") is not None else cell.get("capacity")
        if amount is not None and not positive_number(str(amount)):
            issues.append(f"invalid-{prefix}-amount")
    return issues


def valid_url(value: str) -> bool:
    parsed = urlparse(value)
    return parsed.scheme in {"http", "https"} and bool(parsed.netloc)


def non_negative_integer(value: str) -> bool:
    try:
        parsed = int(value)
    except ValueError:
        return False
    return parsed >= 0


def positive_number(value: str) -> bool:
    try:
        parsed = float(value)
    except ValueError:
        return False
    return parsed > 0


