#!/usr/bin/env python3
"""Tiny local bank reserve signer fixture for Cellshire testnet smoke flows.

This is not a private-key signer. It is a deterministic fixture endpoint for
exercising the frontend `chainBankReserveSignerUrl` path while the real bank
backend and deployed scripts are still being built.

Run:

    python3 scripts/bank_reserve_signer_fixture.py --port 8791

Production smoke bundles can be generated by supplying deployed script params
and real backend URLs:

    python3 scripts/bank_reserve_signer_fixture.py --validate-production-smoke \
        --deployment-values-json bank-testnet-values.json

    python3 scripts/bank_reserve_signer_fixture.py --validate-production-smoke \
        --deployment-values-json bank-testnet-values.json \
        --write-smoke-report bank-testnet-smoke-report.json

or:

    python3 scripts/bank_reserve_signer_fixture.py --validate-production-smoke \
        --script-params-json bank-scripts.json \
        --bank-borrow-input-provider-url https://bank.example/borrow-inputs \
        --bank-repay-input-provider-url https://bank.example/repay-inputs \
        --bank-reserve-signer-url https://bank.example/sign

    python3 scripts/bank_reserve_signer_fixture.py --production-smoke \
        --script-params-json bank-scripts.json \
        --bank-borrow-input-provider-url https://bank.example/borrow-inputs \
        --bank-repay-input-provider-url https://bank.example/repay-inputs \
        --bank-reserve-signer-url https://bank.example/sign

Useful endpoints:

    GET  /health
    GET  /reserve-inputs
    GET  /smoke-params
    POST /borrow-inputs
    POST /repay-inputs
    POST /sign

The frontend should point `chainBankReserveSignerUrl` at `/sign`.
"""

