def smoke_params(config: FixtureConfig) -> dict[str, Any]:
    params = {
        "chainBank": "1",
        "chainBankCollateral": "ckb",
        "chainBankSubmit": "ccc-real",
        "chainCurrencyCkb": "30000",
        "wallet": "joyid",
        "chainBankReserveSignerUrl": config.reserve_signer_url or signer_url(config),
        **config.script_params,
    }
    if config.input_provider_url:
        params["chainBankInputProviderUrl"] = config.input_provider_url
    elif config.borrow_input_provider_url:
        params["chainBankBorrowInputProviderUrl"] = config.borrow_input_provider_url
    else:
        params["chainBankInputProviderUrl"] = borrow_inputs_url(config)
    if config.repay_input_provider_url:
        params["chainBankRepayInputProviderUrl"] = config.repay_input_provider_url
    elif not config.input_provider_url:
        params["chainBankRepayInputProviderUrl"] = repay_inputs_url(config)
    if config.production_smoke:
        params["chainBankScriptMode"] = "production"
    if config.token:
        params["chainBankInputProviderToken"] = config.token
        params["chainBankReserveSignerToken"] = config.token
    return params


def smoke_config(config: FixtureConfig, game_url: str = "") -> dict[str, Any]:
    params = smoke_params(config)
    query = urlencode(params)
    body = {
        "params": params,
        "queryString": query,
    }
    if game_url:
        separator = "&" if "?" in game_url else "?"
        body["url"] = f"{game_url}{separator}{query}"
    return body


