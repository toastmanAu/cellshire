def have_ffmpeg() -> bool:
    return shutil.which("ffmpeg") is not None


def convert_to_ogg(src_wav: Path, dst_ogg: Path) -> None:
    """Convert WAV → Ogg/Vorbis at quality 4 (~96 kbps mono).
    This matches the size/quality envelope of the existing shipped SFX."""
    dst_ogg.parent.mkdir(parents=True, exist_ok=True)
    subprocess.run(
        [
            "ffmpeg",
            "-y",
            "-loglevel",
            "error",
            "-i",
            str(src_wav),
            "-c:a",
            "libvorbis",
            "-q:a",
            "4",
            str(dst_ogg),
        ],
        check=True,
    )


def render_js_snippet(selections: list[SfxSelection]) -> str:
    lines = [
        "// Auto-generated by scripts/install_cellshire_sfx.py — add to src/ui/Audio.js",
        "// inside loadUiAudio() (or a new loadCellshireSfx()) and wire play* wrappers.",
        "await Promise.all([",
    ]
    for sel in selections:
        lines.append(
            f"  registerClip({sel.clip_id!r:<22}, 'sfx/{sel.clip_id}.ogg',"
            f" {{ minIntervalMs: {sel.min_interval_ms} }}),"
        )
    lines.append("]);")
    return "\n".join(lines)


def main() -> int:
    if not SFX_SELECTIONS:
        print("No selections recorded yet — review tmp/sfx-generation/contact-sheet.md")
        print("then edit SFX_SELECTIONS in this file and re-run.")
        return 0

    if not have_ffmpeg():
        print("ERROR: ffmpeg not found on PATH. Install ffmpeg first.", file=sys.stderr)
        return 1

    converted = 0
    missing: list[SfxSelection] = []
    for sel in SFX_SELECTIONS:
        src = TMP_ROOT / sel.clip_id / f"seed{sel.seed}.wav"
        if not src.exists():
            missing.append(sel)
            continue
        dst = ASSETS_SFX / f"{sel.clip_id}.ogg"
        convert_to_ogg(src, dst)
        rel = dst.relative_to(ROOT)
        print(f"  ✓ {sel.clip_id:20s} → {rel}")
        converted += 1

    if missing:
        print()
        for sel in missing:
            print(f"  ✗ missing seed wav for {sel.clip_id} seed={sel.seed}")
        return 2

    print()
    print(f"Installed {converted} clips into {ASSETS_SFX.relative_to(ROOT)}/")
    print()
    print("JS snippet for src/ui/Audio.js:")
    print()
    print(render_js_snippet(SFX_SELECTIONS))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
