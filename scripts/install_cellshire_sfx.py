"""Install selected Cellshire SFX into the game assets directory.

Workflow:

1. After ``run_cellshire_sfx_batch.py`` produces candidates in
   ``tmp/sfx-generation/<clip-id>/seed<N>.wav``, pick the winner per clip.
2. Record the picks in this file's ``SFX_SELECTIONS`` table.
3. Run ``python3 scripts/install_cellshire_sfx.py`` to copy the chosen
   wavs into ``assets/sfx/<clip-id>.ogg``, converting to Ogg/Vorbis for
   compact in-game delivery via the existing audio pipeline (matching
   the format of the current shipped SFX).

The script also emits a ``loadCellshireSfx()`` JS snippet listing the
``registerClip`` lines, so Codex can wire the new clips into
``src/ui/Audio.js`` without manual book-keeping.
"""

